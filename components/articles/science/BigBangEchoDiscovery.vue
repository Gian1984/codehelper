    <h2 class="text-xl font-semibold text-white">What Is the Cosmic Microwave Background?</h2>

    <p class="text-gray-300 text-base leading-relaxed">
      The CMB is electromagnetic radiation that fills the entire universe. It was released about
      380,000 years after the Big Bang, when the universe had cooled enough for electrons and protons
      to combine and form neutral hydrogen atoms. Before this "recombination," the universe was
      an opaque plasma where photons couldn't travel freely. After recombination, light could finally
      travel through space and that light is what we detect today as the CMB.
    </p>

    <div class="bg-gray-900 border border-gray-700 rounded-lg p-4">
      <p class="text-sm text-gray-400 mb-2">CMB Key Facts:</p>
      <ul class="text-sm text-gray-300 space-y-2">
        <li><strong class="text-white">Temperature:</strong> 2.725 Kelvin (about -270.4°C)</li>
        <li><strong class="text-white">Wavelength:</strong> Peaked in microwave range (~1.9 mm)</li>
        <li><strong class="text-white">Age:</strong> Light released ~380,000 years after the Big Bang</li>
        <li><strong class="text-white">Uniformity:</strong> Identical in all directions to 1 part in 100,000</li>
        <li><strong class="text-white">Significance:</strong> Best evidence for the Big Bang theory</li>
      </ul>
