      Plasma turbulence, magnetic reconnection events, Alfvén waves: these are the suspected mechanisms.
      And they are not just astrophysical curiosities. Understanding how the Sun heats and accelerates
      its plasma directly informs the physics models used in fusion reactor design, where controlling
      plasma heating and stability is the defining engineering challenge.
    </p>

