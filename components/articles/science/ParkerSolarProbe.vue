    <h2 class="text-xl font-semibold text-white">The Engineering Challenge: Surviving the Sun</h2>

    <p class="text-gray-300 text-base leading-relaxed">
      Getting close to the Sun is not primarily a propulsion problem: it is a <strong class="text-white">materials problem</strong>.
      The solar corona, the Sun's outer atmosphere, reaches temperatures of over one million degrees Celsius.
      Paradoxically, the density of particles there is so low that the actual heat transfer to a spacecraft
      is far less than those numbers suggest. As Balbi notes, it's like being in a room full of very hot sparks
      that are also very sparse: occasionally you get hit, but the overall heat exchange is manageable.
      Still, "manageable" here means enduring roughly 1,400 °C on the probe's front face, enough to melt
      most metals and destroy any conventional instrument.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      The solution was a <strong class="text-white">carbon composite heat shield</strong> over 11 centimeters thick.
      It reflects the vast majority of solar radiation and acts as a thermal barrier so effective that the
      instruments behind it operate near room temperature. This single component, elegant in its simplicity
      and radical in its performance, is what made the mission possible. And it's where the conversation
      between space exploration and Earth-bound technology becomes genuinely exciting.
    </p>

