        <p class="text-sm mt-1">
          Keynes coined "technological unemployment" during the Depression and predicted a 15-hour work week
          by 2030. The anxiety was real. The actual outcome was the longest sustained period of productivity
          and employment growth in history: the postwar economic boom.
        </p>
      </li>
      <li class="bg-gray-700 p-4 rounded shadow">
        <p class="font-semibold text-white">The Computerization Panic (1980s to 1990s)</p>
        <p class="text-sm mt-1">
          A Second Luddite Congress issued a manifesto in 1996 against "the increasingly bizarre
          and frightening technologies of the Computer Age." Computers eventually transformed
          offices but the occupational mix changed far less than feared in any single decade.
        </p>
      </li>
