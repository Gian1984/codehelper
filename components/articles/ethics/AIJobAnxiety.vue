          in two months. Steam engines took nearly 100 years from first installation to peak adoption.
          The pace of diffusion is unlike anything in prior waves, even if the eventual outcome is uncertain.
        </p>
      </li>
    </ul>

    <h2 class="text-xl font-semibold text-white">A Reflection: The Problem We Need to Name</h2>

    <p class="text-gray-300 text-base leading-relaxed">
      Matt Shumer's essay was not really about the future. It was about the present: the moment when
      a person who builds AI for a living looked at what he had built and felt genuinely disoriented.
      That disorientation, multiplied across tens of millions of workers, is a public health problem
      that is already in progress.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      The policy conversation about AI and employment is almost entirely focused on future scenarios:
      how many jobs will be lost, which sectors are most exposed, what safety nets should be designed.
      These are serious questions. But they are the wrong priority for right now. The worker who is anxious
      today, losing sleep today, "job hugging" today out of fear rather than satisfaction, is being harmed today.
      No future outcome changes that.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      Research in anticipatory grief teaches us something important: chronic low-level fear is
      often more damaging than an acute event. The person who loses their job in a layoff experiences
      a defined crisis with a defined endpoint. The person who spends three years wondering whether
      they will be replaced experiences an open-ended erosion. It never resolves. There is no closure.
      The BCG burnout data suggests this is not hypothetical: the anxiety is already lowering productivity,
      undermining the very argument that AI tools are making workers more effective.
    </p>

    <div class="bg-gradient-to-r from-indigo-900/40 to-purple-900/40 border border-indigo-600/50 rounded-lg p-6">
      <p class="text-gray-300 text-center text-lg">
        We are debating whether AI will take jobs in 2028 while ignoring the damage
        that the fear of AI is doing to workers in 2026. Both problems deserve a response.
        Only one of them is already here.
      </p>
    </div>

    <p class="text-gray-300 text-base leading-relaxed">
      Shumer said his goal was to push people to start experimenting, learning, adapting.
      That is reasonable advice. But it places the burden entirely on the individual worker,
      which is where most of this conversation tends to land. The harder question is institutional:
      what do organizations, governments, and educational systems owe workers who are navigating
      a transition they did not choose and cannot fully control?
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      History suggests the transition will take longer than the most alarmed predictions,
      and that new work will eventually emerge. It also suggests the workers who bear the cost
      of the transition rarely benefit proportionally from the gains that follow.
      The Luddites were not wrong about what was happening. They were wrong about what could stop it.
      The question worth asking now is not whether AI will transform work, but how we choose
      to distribute the cost of that transformation while it is happening, not after.
    </p>

