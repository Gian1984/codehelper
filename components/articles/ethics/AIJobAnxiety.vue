    <h2 class="text-xl font-semibold text-white">The Psychology of Anticipatory Loss</h2>

    <p class="text-gray-300 text-base leading-relaxed">
      What makes AI-related anxiety structurally different from ordinary job insecurity is its
      <strong class="text-white">anticipatory nature</strong>. Classical job loss anxiety is triggered by
      an event: a layoff notice, a restructuring announcement, a performance review. It is reactive.
      AI anxiety is proactive: it begins before any event occurs, activated by the perception that
      one's skills are degrading in real time, that the ground is shifting beneath you even as you stand still.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      A peer-reviewed study published in PMC (2025) examining AI-induced displacement anxiety among
      IT professionals found this pattern confirmed in clinical terms. Participants described layered
      psychological disruptions: acute shock at AI's capabilities, erosion of professional identity
      (when machines perform the tasks that defined their expertise), and what researchers identified
      as classic signs of <strong class="text-white">anticipatory grief</strong>, including repetitive thoughts
      about financial insecurity, skill irrelevance, and employability. Crucially, these symptoms
      appeared before any actual displacement. The threat was sufficient to trigger the response.
    </p>

    <div class="bg-yellow-900/30 border border-yellow-600/50 rounded-lg p-4">
      <p class="text-sm text-yellow-300">
        <strong class="text-white">Key finding (Frontiers in Psychology, 2026):</strong> A paper on
        "algorithmic anxiety" found that the anticipatory nature of AI-related threat is specifically
        corrosive because it depletes coping resources chronically, before any event actually occurs.
        Workers experience a kind of pre-traumatic stress: the wound arrives before the injury.
      </p>
    </div>

    <p class="text-gray-300 text-base leading-relaxed">
      A separate study found that the more human-like and personalized AI tools become, the more they
      amplify job replacement anxiety. This creates a perverse dynamic: the more helpful and capable AI
      becomes, the more threatening it psychologically appears, even to workers who are actively benefiting from it.
    </p>

