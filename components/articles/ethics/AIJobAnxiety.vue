    <h2 class="text-xl font-semibold text-white">What the Economists Actually Say</h2>

    <p class="text-gray-300 text-base leading-relaxed">
      It is worth stepping back from the panic and examining what the most rigorous economists have actually found.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      <strong class="text-white">Daron Acemoglu</strong> (MIT, Nobel Laureate in Economics) is one of the
      most prominent skeptics of AI disruption narratives. His quantitative estimate is that AI will increase
      total factor productivity by no more than 0.66% over ten years. His diagnosis of the problem is structural:
      firms are using AI primarily for automation (replacing workers) rather than for machine usefulness
      (augmenting worker expertise). His conclusion is not optimistic by default but conditional:
      the outcome depends heavily on policy choices we have not yet made.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      <strong class="text-white">David Autor</strong> (MIT) takes a more hopeful long view. His argument
      is that AI could reverse four decades of job polarization and rebuild the middle class, but only if
      developed as a collaboration tool rather than a pure automation engine. His historical reading:
      the Industrial Revolution took roughly 60 years to benefit rank-and-file workers. Computers
      transformed offices but took nearly a decade after their public release to become commonplace.
      Every major technology wave eventually created jobs that did not exist before.
    </p>

    <p class="text-gray-300 text-base leading-relaxed">
      The Yale Budget Lab's assessment is sobering in its precision: in the 33 months since ChatGPT's release,
      the broader labor market has not experienced a discernible aggregate disruption. But employment growth
      in marketing, graphic design, office administration, and call centers has fallen below trend.
      The broad numbers look stable. The specific, exposed sectors are already contracting.
    </p>

