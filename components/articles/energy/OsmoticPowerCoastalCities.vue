    <h2 class="text-xl font-semibold text-white">How Osmotic Power Works</h2>

    <p class="text-gray-300 text-base leading-relaxed">
      The basic principle is simple: place freshwater on one side, saltwater on the other, and a semipermeable membrane
      in between. The freshwater naturally moves toward the saltier side. This movement creates either pressure or
