    </p>

    <h3 class="text-lg font-semibold text-indigo-400">1. PRO (Pressure Retarded Osmosis)</h3>

    <div class="bg-gray-900 border border-gray-700 rounded-lg p-4">
      <p class="text-sm text-gray-400 mb-2">How PRO works:</p>
      <ul class="text-sm text-gray-300 space-y-2">
        <li><strong class="text-white">Step 1:</strong> Freshwater enters chamber A, saltwater enters chamber B</li>
        <li><strong class="text-white">Step 2:</strong> A semipermeable membrane separates them (only water can pass)</li>
        <li><strong class="text-white">Step 3:</strong> Freshwater flows into the saltwater side through osmosis</li>
        <li><strong class="text-white">Step 4:</strong> Volume increases on the salty side, building pressure</li>
        <li><strong class="text-white">Step 5:</strong> Pressurized water spins a turbine</li>
        <li><strong class="text-white">Step 6:</strong> Turbine drives a generator, producing electricity</li>
      </ul>
    </div>

    <p class="text-gray-300 text-base leading-relaxed mt-4">
      Think of it as a <strong class="text-white">mini hydroelectric dam</strong>, but instead of gravity creating the
      water pressure, osmosis does the work. The "waterfall" is chemical, not physical.
    </p>

    <h3 class="text-lg font-semibold text-indigo-400 mt-6">2. Ionic Generators (Battery-like)</h3>

    <div class="bg-gray-900 border border-gray-700 rounded-lg p-4">
      <p class="text-sm text-gray-400 mb-2">How ionic generators work:</p>
      <ul class="text-sm text-gray-300 space-y-2">
        <li><strong class="text-white">Step 1:</strong> Salt (NaCl) dissolves into Na⁺ (positive) and Cl⁻ (negative) ions</li>
        <li><strong class="text-white">Step 2:</strong> Specialized membranes separate these ions</li>
        <li><strong class="text-white">Step 3:</strong> One side accumulates positive charges, the other negative</li>
        <li><strong class="text-white">Step 4:</strong> This creates a voltage difference (like a battery)</li>
        <li><strong class="text-white">Step 5:</strong> Electrons flow through a circuit, generating electricity</li>
      </ul>
    </div>

    <p class="text-gray-300 text-base leading-relaxed mt-4">
      This approach is essentially a <strong class="text-white">natural battery powered by water</strong>. No turbines,
      no moving parts just electrochemistry at work. It's more compact and potentially more efficient than PRO.
    </p>

